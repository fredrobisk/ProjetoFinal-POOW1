package model;

public class ResumoAcademia {

    private int totalAlunos;
    private int totalPlanosAtivos;
    private int totalTreinosAtivos;

    public int getTotalAlunos() {
        return totalAlunos;
    }

    public void setTotalAlunos(int totalAlunos) {
        this.totalAlunos = totalAlunos;
    }


    public int getTotalPlanosAtivos() {
        return totalPlanosAtivos;
    }

    public void setTotalPlanosAtivos(int totalPlanosAtivos) {
        this.totalPlanosAtivos = totalPlanosAtivos;
    }


    public int getTotalTreinosAtivos() {
        return totalTreinosAtivos;
    }

    public void setTotalTreinosAtivos(int totalTreinosAtivos) {
        this.totalTreinosAtivos = totalTreinosAtivos;
    }
}